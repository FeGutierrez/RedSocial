public class Coordenada {
    private int x;
    private int y;
    private Estudiante usuarioEtiquetado;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public Estudiante getUsuarioEtiquetado() {
        return usuarioEtiquetado;
    }

    public void setUsuarioEtiquetado(Estudiante usuarioEtiquetado) {
        this.usuarioEtiquetado = usuarioEtiquetado;
    }
    
    
    
}

